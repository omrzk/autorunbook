import { marked } from "marked";
import { RunbookContent, SECTION_ORDER } from "./db";

export interface ExportMeta {
  runbookId: number;
  version: number;
  status: string;
  author: string;
  createdAt: string;
  approvedBy?: string;
}

export function toMarkdown(c: RunbookContent, meta: ExportMeta): string {
  const lines: string[] = [];
  lines.push(`# ${c.title}`);
  lines.push("");
  lines.push(
    `> **Severity:** ${c.severity.toUpperCase()} · **Version:** v${meta.version} · **Status:** ${meta.status} · **Generated:** ${meta.createdAt}`
  );
  if (c.tags.length) lines.push(`> **Tags:** ${c.tags.join(", ")}`);
  if (meta.approvedBy) lines.push(`> **Approved by:** ${meta.approvedBy}`);
  lines.push("");
  for (const s of SECTION_ORDER) {
    const body = c[s.key]?.trim();
    if (!body) continue;
    lines.push(`## ${s.label}`);
    lines.push("");
    lines.push(body);
    lines.push("");
  }
  lines.push("---");
  lines.push(
    `*Generated by [AutoRunbook](https://github.com/omrzk/autorunbook) — runbook #${meta.runbookId} v${meta.version}.*`
  );
  return lines.join("\n");
}

