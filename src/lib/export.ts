export function toHtml(c: RunbookContent, meta: ExportMeta): string {
  marked.setOptions({ gfm: true, breaks: false });
  const sections = SECTION_ORDER.map((s) => {
    const body = c[s.key]?.trim();
    if (!body) return "";
    return `<section><h2>${escapeHtml(s.label)}</h2>${marked.parse(body)}</section>`;
  }).join("\n");

  return `<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="utf-8">
<meta name="viewport" content="width=device-width, initial-scale=1">
<title>${escapeHtml(c.title)} — AutoRunbook</title>
<style>${HTML_STYLE}</style>
</head>
<body>
<div class="page">
  <h1>${escapeHtml(c.title)}</h1>
  <div class="meta">
    <span class="badge sev-${c.severity}">${c.severity}</span>
    <span class="badge status-${meta.status}">${meta.status.replace("_", " ")}</span>
    <span class="badge">v${meta.version}</span>
    ${c.tags.map((t) => `<span class="badge">${escapeHtml(t)}</span>`).join("")}
  </div>
  ${sections}
  <footer>Generated by AutoRunbook — runbook #${meta.runbookId} v${meta.version} · ${escapeHtml(meta.createdAt)}${meta.approvedBy ? ` · approved by ${escapeHtml(meta.approvedBy)}` : ""}</footer>
</div>
</body>
</html>`;
}

export function escapeHtml(s: string): string {
  return s
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}
